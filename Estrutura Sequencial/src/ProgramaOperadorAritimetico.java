
public class ProgramaOperadorAritimetico {

	public static void main(String[] args) {
		
		int numero = 9;
		int numero2 = 3;
		
		System.out.println("Soma " + numero + " + " + numero2 + " = " + (numero+numero2));
		System.out.println("Subtração " + numero + " - " + numero2 + " = " + (numero-numero2));
		System.out.println("Divisao " + numero + " / " + numero2 + " = " + (numero/numero2));
		System.out.println("Multiplicação " + numero + " x " + numero2 + " = " + (numero*numero2));
		System.out.println("Resto da Divisão " + numero + " / " + numero2 + " = " + (numero%numero2));

	}

}
